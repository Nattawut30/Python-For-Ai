""" INTEGERS - INT: Whole numbers without decimals """
age = 25
score = 30
print(score)

""" FLOATS: Numbers with decimal points """
price = 19.99
temperature = -5.5
pi = 3.14159
print(pi)

""" Basic Math Operations"""
total = 10 + 5
print(total)

change = 20 - 7
print(change)

area = 6 * 4
print(area)
half = 18 / 2
print(half)

squared = 5 ** 2
print(squared)
cubed = 2 ** 3
print(cubed)
 
""" 
STRING: Text or any characters inside quotes
Python doesn't care if you use single or double quotes
Just be consistent.
"""

name = "Alice"
message = 'Hello, Alice!'
print(message)

first_name = "Fluke"
last_name = "Inwza007"
full_name = first_name + " " + last_name
print(full_name)

long_dash = "*" * 30
print(long_dash)

len(long_dash)

""" BOOLEANS: True/False """

is_logged_in = True
is_admin = False
has_permission = True
print(has_permission)

age = 25
can_vote = age >= 18
print(can_vote)

""" Comparison Operators """
print(age == 25) # True - equal
print(age != 30) # True - not equals
print(age > 20) # True - Greater
print(age < 30) # True - Less than
print(age >= 25) # True - Greater or equal
print(age <= 22) # True - less or equal
